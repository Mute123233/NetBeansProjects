    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String login = jFormattedTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        // –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ (–µ—Å–ª–∏ –Ω—É–∂–Ω–æ –æ—Å—Ç–∞–≤–∏—Ç—å —Ö–∞—Ä–¥–∫–æ–¥)
        if (login.equals("admin") && password.equals("admin")) {
            this.dispose();
            new admin1().setVisible(true);
            return;
        }

        try {
            // –°–æ–∑–¥–∞–µ–º HTTP –∫–ª–∏–µ–Ω—Ç
            CloseableHttpClient httpClient = HttpClients.createDefault();

            // –°–æ–∑–¥–∞–µ–º POST –∑–∞–ø—Ä–æ—Å
            HttpPost httpPost = new HttpPost("http://localhost:8080/atelie/getAuthorization");

            // –î–æ–±–∞–≤–ª—è–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä—ã
            List<NameValuePair> params = new ArrayList<>();
            params.add(new BasicNameValuePair("Login", login));
            params.add(new BasicNameValuePair("Password", password));
            httpPost.setEntity(new UrlEncodedFormEntity(params, "UTF-8"));

            // –í—ã–ø–æ–ª–Ω—è–µ–º –∑–∞–ø—Ä–æ—Å
            CloseableHttpResponse response = httpClient.execute(httpPost);

            // –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –æ—Ç–≤–µ—Ç
            String responseBody = EntityUtils.toString(response.getEntity());
            int statusCode = response.getStatusLine().getStatusCode();

            // –ó–∞–∫—Ä—ã–≤–∞–µ–º —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ
            response.close();
            httpClient.close();

            if (statusCode == 200 && responseBody != null) {
                // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–æ–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–µ—Ä–µ–∑ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ API –∑–∞–ø—Ä–æ—Å—ã
                checkUserRole(login);
            } else {
                JOptionPane.showMessageDialog(this, "–ù–µ–≤–µ—Ä–Ω—ã–π –ª–æ–≥–∏–Ω/–ø–∞—Ä–æ–ª—å");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ —Å–µ—Ä–≤–µ—Ä—É: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private void checkUserRole(String login) {
        try {
            CloseableHttpClient httpClient = HttpClients.createDefault();

            // –°–Ω–∞—á–∞–ª–∞ –ø–æ–ª—É—á–∞–µ–º ID —à–≤–µ–∏ –ø–æ –ª–æ–≥–∏–Ω—É
            HttpPost getShveyaId = new HttpPost("http://localhost:8080/atelie/getShveyaIdByLogin");
            List<NameValuePair> shveyaIdParams = new ArrayList<>();
            shveyaIdParams.add(new BasicNameValuePair("login", login));
            getShveyaId.setEntity(new UrlEncodedFormEntity(shveyaIdParams, "UTF-8"));

            CloseableHttpResponse shveyaIdResponse = httpClient.execute(getShveyaId);
            String shveyaIdResponseBody = EntityUtils.toString(shveyaIdResponse.getEntity());
            shveyaIdResponse.close();

            // –ü–∞—Ä—Å–∏–º JSON –æ—Ç–≤–µ—Ç
            JSONObject shveyaJson = new JSONObject(shveyaIdResponseBody);
            boolean isShveya = shveyaJson.getBoolean("success");

            if (isShveya) {
                int shveyaId = shveyaJson.getInt("shveyaId");
                this.dispose();
                new shveya(shveyaId).setVisible(true); // –ü–ï–†–ï–î–ê–ï–ú ID!
                return;
            }

            // –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è –ø—Ä–æ–¥–∞–≤—Ü–∞
            HttpPost getProdavecId = new HttpPost("http://localhost:8080/atelie/getProdavecIdByLogin");
            List<NameValuePair> prodavecIdParams = new ArrayList<>();
            prodavecIdParams.add(new BasicNameValuePair("login", login));
            getProdavecId.setEntity(new UrlEncodedFormEntity(prodavecIdParams, "UTF-8"));

            CloseableHttpResponse prodavecIdResponse = httpClient.execute(getProdavecId);
            String prodavecIdResponseBody = EntityUtils.toString(prodavecIdResponse.getEntity());
            prodavecIdResponse.close();

            JSONObject prodavecJson = new JSONObject(prodavecIdResponseBody);
            boolean isProdavec = prodavecJson.getBoolean("success");

            if (isProdavec) {
                int prodavecId = prodavecJson.getInt("prodavecId");
                this.dispose();
                new prodavec(prodavecId).setVisible(true); // –ü–ï–†–ï–î–ê–ï–ú ID!
                return;
            }

            // –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è —É–ø—Ä–∞–≤–ª—è—é—â–µ–≥–æ
            HttpPost getUpravId = new HttpPost("http://localhost:8080/atelie/getUpravlyayushchiyIdByLogin");
            List<NameValuePair> upravIdParams = new ArrayList<>();
            upravIdParams.add(new BasicNameValuePair("login", login));
            getUpravId.setEntity(new UrlEncodedFormEntity(upravIdParams, "UTF-8"));

            CloseableHttpResponse upravIdResponse = httpClient.execute(getUpravId);
            String upravIdResponseBody = EntityUtils.toString(upravIdResponse.getEntity());
            upravIdResponse.close();

            JSONObject upravJson = new JSONObject(upravIdResponseBody);
            boolean isUpravlyayushchiy = upravJson.getBoolean("success");

            if (isUpravlyayushchiy) {
                int upravId = upravJson.getInt("upravlyayushchiyId");
                this.dispose();
                new upravlyayushchiy(upravId).setVisible(true); // –ü–ï–†–ï–î–ê–ï–ú ID!
                return;
            }

            httpClient.close();

            JOptionPane.showMessageDialog(this, "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –∏–º–µ–µ—Ç –Ω–∞–∑–Ω–∞—á–µ–Ω–Ω–æ–π —Ä–æ–ª–∏");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "–û—à–∏–±–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ —Ä–æ–ª–∏: " + ex.getMessage());
            ex.printStackTrace();
        }
    }

